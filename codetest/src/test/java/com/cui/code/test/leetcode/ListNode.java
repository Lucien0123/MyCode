package com.cui.code.test.leetcode;

/**
 * Definition for singly-linked list.
 *
 * @author cuishixiang
 * @date 2019-02-28
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode(int x) {
        val = x;
    }


    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
